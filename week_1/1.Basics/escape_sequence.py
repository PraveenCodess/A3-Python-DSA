"""
Escape Sequences (always comes in "" , '')
\n -> New line
\t -> Tab
\\ -> \
\" -> "
\' -> '
"""
# My name is "Anirudh" Khurana
print('My name is "Anirudh" khurana')
print('My name is "Anirudh" khurana')

# My name is 'Anirudh' Khurana
print("My name is 'Anirudh' Khurana")
# My name is A\nirudh
print("My name is A\\\\\\nirudh")
print("Hello\n World\nOK done")
print("Python is\t\t\t\tgood")
print("Py/\\thon")
print("Py\"\\\"/\"\\\"thon")